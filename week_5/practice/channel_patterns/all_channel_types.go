func example5_ClosingChannel() {
	fmt.Println("â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”")
	fmt.Println("5ï¸âƒ£  CLOSING CHANNEL")
	fmt.Println("â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”â”")

	ch := make(chan int, 3)

	// ĞŸĞ¸ÑˆĞµĞ¼Ğ¾ Ğ´Ğ°Ğ½Ñ–
	ch <- 1
	ch <- 2
	ch <- 3

	// Ğ—Ğ°ĞºÑ€Ğ¸Ğ²Ğ°Ñ”Ğ¼Ğ¾ ĞºĞ°Ğ½Ğ°Ğ»
	close(ch) // â¬…ï¸ ĞŸÑ–ÑĞ»Ñ close() Ğ½Ğµ Ğ¼Ğ¾Ğ¶Ğ½Ğ° Ğ±Ñ–Ğ»ÑŒÑˆĞµ Ğ¿Ğ¸ÑĞ°Ñ‚Ğ¸!

	// âŒ ch <- 4 // panic: send on closed channel

	// âœ… ĞĞ»Ğµ ĞœĞĞ–ĞĞ Ñ‡Ğ¸Ñ‚Ğ°Ñ‚Ğ¸ Ğ· Ğ·Ğ°ĞºÑ€Ğ¸Ñ‚Ğ¾Ğ³Ğ¾ ĞºĞ°Ğ½Ğ°Ğ»Ñƒ!
	fmt.Println("  Reading from closed channel:")
	fmt.Println("    ", <-ch) // 1
	fmt.Println("    ", <-ch) // 2
	fmt.Println("    ", <-ch) // 3
	fmt.Println("    ", <-ch) // 0 (zero value Ğ´Ğ»Ñ int)
	fmt.Println("    ", <-ch) // 0 (Ğ·Ğ°Ğ²Ğ¶Ğ´Ğ¸ Ğ¿Ğ¾Ğ²ĞµÑ€Ñ‚Ğ°Ñ” zero value)

	// ĞŸĞµÑ€ĞµĞ²Ñ–Ñ€ĞºĞ°, Ñ‡Ğ¸ ĞºĞ°Ğ½Ğ°Ğ» Ğ·Ğ°ĞºÑ€Ğ¸Ñ‚Ğ¸Ğ¹ (comma ok idiom)
	value, ok := <-ch
	if !ok {
		fmt.Println("  âœ… Channel is closed (ok = false)")
	} else {
		fmt.Printf("  Value: %d\n", value)
	}
	fmt.Println()
}

